    public void ItemCosto()
    {
        double sum = 0;
        
        for (int i = 0; i < jTable1.getRowCount(); i++)
        {
            sum = sum + Double.parseDouble(jTable1.getValueAt(i, 2).toString());
        }
        txtSubTotal.setText(Double.toString(sum));
        double cTotal1 = Double.parseDouble(txtSubTotal.getText());
        
        double cIGV = (cTotal1 * 0.18);
        String iIGVTotal = String.format("S/ %.2f", cIGV);
        txtIGV.setText(iIGVTotal);
        
        String iSubTotal = String.format("S/ %.2f", cTotal1);
        txtSubTotal.setText(iSubTotal);
        
        String iTotal = String.format("S/ %.2f", cTotal1 + cIGV);
        txtTotal.setText(iTotal);
        
        String BarCode = String.format("El total es %.2f", cTotal1 + cIGV);
        txtBarCode.setText(BarCode);
    }
    
