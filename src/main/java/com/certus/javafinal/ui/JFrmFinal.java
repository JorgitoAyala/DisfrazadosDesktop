    public void Cambio()
    {
        double sum = 0;
        double igv = 0.18;
        double efectivo = Double.parseDouble(txtMonto.getText());
        
        for (int i = 0; i < jTable1.getRowCount(); i++)
        {
            sum = sum + Double.parseDouble(jTable1.getValueAt(i, 2).toString());
        }
        
        double cIGV = (sum * igv);
        double cVuelto = (efectivo - (sum + cIGV));
        
        String VueltoRecibido = String.format("S/ %.2f", cVuelto);
        txtVuelto.setText(VueltoRecibido);
    }
    
